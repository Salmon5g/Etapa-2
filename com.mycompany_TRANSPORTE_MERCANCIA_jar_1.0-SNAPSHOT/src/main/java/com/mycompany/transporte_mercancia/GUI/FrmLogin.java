/**
     * Maneja el evento del botón "Ingresar".
     * Valida que los campos RUT y contraseña no estén vacíos, consulta
     * las credenciales en la base de datos mediante {@link DAOPersonal#login(String, String)}
     * y, si son correctas, abre el formulario {@link Principal} pasando el
     * objeto {@link Personal} autenticado, cerrando el formulario de login.
     * <p>
     * Si los campos están vacíos o las credenciales son incorrectas,
     * muestra un mensaje de error informativo al usuario.
     *
     * @param evt Evento de acción generado por el botón btn_ingresar.
     */
    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        String rut = txt_rut.getText().trim();
        String contrasena = new String(txt_contrasena.getPassword()).trim();

        if (rut.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Por favor ingrese RUT y contraseña.",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        com.mycompany.transporte_mercancia.Data.DAOPersonal dao =
                new com.mycompany.transporte_mercancia.Data.DAOPersonal();
        com.mycompany.transporte_mercancia.Logica.Personal personal = dao.login(rut, contrasena);

        if (personal == null) {
            JOptionPane.showMessageDialog(this,
                    "RUT o contraseña incorrectos.",
                    "Error de acceso", JOptionPane.ERROR_MESSAGE);
            return;
        }

       
        new com.mycompany.transporte_mercancia.GUI.Principal(personal).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btn_ingresarActionPerformed

