/**
     * Maneja el evento de clic sobre una fila de la tabla de conductores.
     * Recupera el ID del conductor seleccionado, rellena los campos del
     * formulario con sus datos (RUT, nombre y contraseña) y activa el modo
     * edición.
     *
     * @param evt Evento de ratón generado al hacer clic en la tabla
     * tbl_conductores.
     */
    private void tbl_conductoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_conductoresMouseClicked
        int selectedRow = this.tbl_conductores.getSelectedRow();
        if (selectedRow != -1) {
            Integer id_seleccionado = (Integer) this.tbl_conductores.getValueAt(selectedRow, 0);
            this.txt_id_conductor.setText(id_seleccionado.toString());

            for (Personal p : dp.listarPorRol("conductor")) {
                if (p.getIdPersonal() == id_seleccionado) {
                    this.txt_rut.setText(p.getRut());
                    this.txt_nombre.setText(p.getNombre());
                    this.txt_contrasena.setText(p.getContrasena());

                }
            }
            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_conductoresMouseClicked
    /**
     * Maneja el evento del botón "Registrar". Valida el RUT ingresado (formato
     * y dígito verificador) y verifica que no exista previamente en el sistema
     * antes de crear un nuevo conductor.
     * <p>
     * El rol asignado automáticamente es {@code "conductor"}. Tras el registro
     * exitoso recarga la tabla y limpia el formulario.
     *
     * @param evt Evento de acción generado por el botón bt_registrar.
     */
    private void bt_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrarActionPerformed
        String rutIngresado = txt_rut.getText().trim();

        if (!validarRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this, "El RUT ingresado no es válido.\nEjemplo: 12.345.678-5",
                    "Rut inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (dp.existeRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this, "El RUT ingresado ya existe en el sistema.",
                    "RUT duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Personal p = new Personal();
        p.setRut(this.txt_rut.getText().trim());
        p.setNombre(this.txt_nombre.getText().trim());
        p.setContrasena(this.txt_contrasena.getText().trim());
        p.setRol("conductor");

        dp.create(p);

        JOptionPane.showMessageDialog(this, "Conductor registrado correctamente.");
        cargarTabla();
        limpiarFormulario();
    }//GEN-LAST:event_bt_registrarActionPerformed
    /**
     * Maneja el evento del botón "Guardar Cambios". Actualiza los datos del
     * conductor seleccionado en la base de datos.
     * <p>
     * Busca el conductor por ID entre los de rol "conductor", verifica que el
     * nuevo RUT no esté duplicado y persiste los cambios con
     * {@link DAOPersonal#update(Personal)}. Luego recarga la tabla, activa el
     * modo nuevo y limpia el formulario.
     *
     * @param evt Evento de acción generado por el botón bt_guardar_cambios.
     */
    private void bt_guardar_cambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardar_cambiosActionPerformed
        String rutIngresado = txt_rut.getText().trim();
        Integer id_seleccionado = Integer.parseInt(this.txt_id_conductor.getText());

        // 1. Validar formato del RUT
        if (!validarRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this, "El RUT ingresado no es válido.\nEjemplo: 12.345.678-5",
                    "Rut inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 2. Verificar duplicado excluyendo al propio conductor
        if (dp.existeRut(rutIngresado, id_seleccionado)) {
            JOptionPane.showMessageDialog(this, "El RUT ingresado ya existe en el sistema.",
                    "RUT duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Buscar el Personal y actualizar
        Personal p = null;
        for (Personal busqueda : dp.listarPorRol("conductor")) {
            if (busqueda.getIdPersonal() == id_seleccionado) {
                p = busqueda;
                break;
            }
        }

        if (p != null) {
            p.setRut(rutIngresado);
            p.setNombre(this.txt_nombre.getText().trim());
            p.setContrasena(this.txt_contrasena.getText().trim());
            p.setRol("conductor");

            dp.update(p);

            JOptionPane.showMessageDialog(this, "Conductor actualizado correctamente.");
            cargarTabla();
            activarModoNuevo();
            limpiarFormulario();
        }
    }//GEN-LAST:event_bt_guardar_cambiosActionPerformed
