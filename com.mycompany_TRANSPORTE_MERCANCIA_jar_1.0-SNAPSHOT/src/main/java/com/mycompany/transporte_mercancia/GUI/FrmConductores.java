    /**
     * Formatea un RUT eliminando puntos y guión, y lo devuelve en el formato
     * {@code NNNNNNNN-D} (número seguido de guión y dígito verificador en
     * mayúscula).
     *
     * @param rut RUT en cualquier formato de entrada.
     * @return RUT formateado como {@code "12345678-5"} o {@code "12345678-K"}.
     */
    public String formatearRut(String rut) {
        rut = rut.replace(".", "").replace("-", "").toUpperCase();
        String numero = rut.substring(0, rut.length() - 1);
        String dv = rut.substring(rut.length() - 1);
        return numero + "-" + dv;
    }

    /**
     * Verifica que el RUT tenga el formato básico {@code NNNNNNNN-D}, es decir,
     * dígitos seguidos de un guión y un dígito o la letra K.
     *
     * @param rut RUT a validar (sin puntos, con guión).
     * @return {@code true} si el formato es correcto; {@code false} en caso
     * contrario.
     */
    public boolean validarFormato(String rut) {
        return rut.matches("^[0-9]+-[0-9K]$");
    }

    /**
     * Valida el RUT completo verificando el dígito verificador.
     *
     * @param rut RUT ingresado
     * @return true si el RUT es válido
     */
    public boolean validarRut(String rut) {
        if (rut == null || rut.trim().isEmpty()) {
            return false;
        }

        rut = rut.replace(".", "").replace("-", "").toUpperCase();

        if (!rut.matches("^\\d{7,8}[0-9K]$")) {
            return false;
        }

        String numero = rut.substring(0, rut.length() - 1);  // todo menos DV
        String dvIngresado = rut.substring(rut.length() - 1);

        String dvCalculado = calcularDV(numero);

        return dvIngresado.equals(dvCalculado);
    }

    /**
     * Calcula el dígito verificador (DV) del RUT.
     *
     * @param rut número del RUT sin DV
     * @return dígito verificador calculado
     */
    // Cálculo tradicional del DV
    public String calcularDV(String rut) {
        int suma = 0;
        int multiplicador = 2;

        for (int i = rut.length() - 1; i >= 0; i--) {
            suma += Character.getNumericValue(rut.charAt(i)) * multiplicador;
            multiplicador = (multiplicador == 7) ? 2 : multiplicador + 1;
        }

        int residuo = 11 - (suma % 11);

        if (residuo == 11) {
            return "0";
        }
        if (residuo == 10) {
            return "K";
        }
        return String.valueOf(residuo);
    }

    /**
     * Carga y actualiza la tabla {@code tbl_conductores} con todos los
     * conductores registrados en la base de datos (filtrados por rol
     * "conductor").
     * <p>
     * Las columnas mostradas son: ID, RUT y Nombre.
     */
    public void cargarTabla() {

        Object[] nombresColumnas = {"ID", "RUT", "Nombre"};
        DefaultTableModel modelo = new DefaultTableModel(nombresColumnas, 0);

        for (Personal p : dp.listarPorRol("conductor")) {
            Object[] row = {p.getIdPersonal(), p.getRut(), p.getNombre()};
            modelo.addRow(row);
        }

        this.tbl_conductores.setModel(modelo);
    }

