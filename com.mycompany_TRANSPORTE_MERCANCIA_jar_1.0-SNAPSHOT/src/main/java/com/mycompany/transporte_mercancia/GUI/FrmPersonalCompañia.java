/**
     * Maneja el evento de clic sobre la tabla de personal. Al seleccionar una
     * fila carga los datos del empleado (nombre, RUT, contraseña y rol) en los
     * campos del formulario y activa el modo edición.
     *
     * @param evt evento de ratón generado al hacer clic en la tabla
     */

    private void tbl_personalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_personalMouseClicked
        int fila = tbl_personal.getSelectedRow();
        if (fila != -1) {
            Integer id = (Integer) tbl_personal.getValueAt(fila, 0);
            txt_id.setText(id.toString());

            for (Personal p : dao.listarTodos()) {
                if (p.getIdPersonal() == id) {
                    txt_nombre.setText(p.getNombre());
                    txt_rut.setText(p.getRut());
                    txt_contrasena.setText(p.getContrasena());
                    cmb_rol.setSelectedItem(p.getRol());
                    break;
                }
            }
            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_personalMouseClicked
    /**
     * Maneja el evento del botón "Registrar". Valida que los campos no estén
     * vacíos, que el RUT tenga formato correcto y que no esté duplicado en la
     * base de datos. Si todas las validaciones pasan, crea un nuevo
     * {@link Personal} y lo persiste. Muestra mensajes de error o confirmación
     * según corresponda.
     *
     * @param evt evento de acción generado por el botón bt_registrar
     */
    private void bt_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrarActionPerformed
        String rutIngresado = txt_rut.getText().trim();

        if (txt_nombre.getText().isEmpty() || txt_rut.getText().isEmpty()
                || txt_contrasena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos.",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

// Validar que el nombre solo contenga letras y espacios
        if (!txt_nombre.getText().trim().matches("[a-zA-ZáéíóúÁÉÍÓÚñÑüÜ ]+")) {
            JOptionPane.showMessageDialog(this,
                    "El nombre solo puede contener letras.",
                    "Nombre inválido",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (!validarRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this,
                    "El RUT ingresado no es válido.\nEjemplo: 12.345.678-5",
                    "Rut inválido",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

// Validación RUT único
        if (dao.existeRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this,
                    "El RUT ingresado ya existe en el sistema.",
                    "RUT duplicado",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Personal p = new Personal();
        p.setNombre(txt_nombre.getText());
        p.setRut(txt_rut.getText());
        p.setContrasena(txt_contrasena.getText());
        p.setRol((String) cmb_rol.getSelectedItem());

        dao.create(p);
        JOptionPane.showMessageDialog(this, "Personal registrado correctamente.");
        cargarTabla();
        limpiarFormulario();

    }//GEN-LAST:event_bt_registrarActionPerformed
    /**
     * Maneja el evento del botón "Modificar". Construye un objeto
     * {@link Personal} con el ID del registro seleccionado y los datos
     * actualizados, luego invoca la actualización en la base de datos. Al
     * finalizar recarga la tabla, limpia el formulario, activa el modo nuevo y
     * muestra un mensaje de confirmación.
     *
     * @param evt evento de acción generado por el botón bt_modificar
     */
    private void bt_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modificarActionPerformed
        String rutIngresado = txt_rut.getText().trim();
        int idSeleccionado = Integer.parseInt(txt_id.getText());

        // 1. Validar campos vacíos
        if (txt_nombre.getText().isEmpty() || txt_rut.getText().isEmpty()
                || txt_contrasena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Complete todos los campos.",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Validar que el nombre solo contenga letras y espacios
        if (!txt_nombre.getText().trim().matches("[a-zA-ZáéíóúÁÉÍÓÚñÑüÜ ]+")) {
            JOptionPane.showMessageDialog(this,
                    "El nombre solo puede contener letras.",
                    "Nombre inválido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Validar formato y dígito verificador del RUT
        if (!validarRut(rutIngresado)) {
            JOptionPane.showMessageDialog(this,
                    "El RUT ingresado no es válido.\nEjemplo: 12.345.678-5",
                    "Rut inválido", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // 4. Verificar duplicado excluyendo el propio registro
        if (dao.existeRut(rutIngresado, idSeleccionado)) {
            JOptionPane.showMessageDialog(this,
                    "El RUT ingresado ya existe en el sistema.",
                    "RUT duplicado", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 5. Recién construir y actualizar
        Personal p = new Personal();
        p.setIdPersonal(idSeleccionado);
        p.setNombre(txt_nombre.getText().trim());
        p.setRut(rutIngresado);
        p.setContrasena(txt_contrasena.getText().trim());
        p.setRol((String) cmb_rol.getSelectedItem());

        dao.update(p);
        cargarTabla();
        limpiarFormulario();
        activarModoNuevo();
        JOptionPane.showMessageDialog(this, "Personal actualizado correctamente.");
    }//GEN-LAST:event_bt_modificarActionPerformed
