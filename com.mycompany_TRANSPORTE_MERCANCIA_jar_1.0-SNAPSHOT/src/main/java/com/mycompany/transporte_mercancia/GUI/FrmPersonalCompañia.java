    /**
     * Carga y refresca la tabla de personal con todos los registros obtenidos
     * desde la base de datos. Define las columnas "ID", "Nombre", "RUT" y
     * "Rol", construye el modelo y lo asigna a la {@code JTable} del
     * formulario.
     */
    public void cargarTabla() {
        Object[] columnas = {"ID", "Nombre", "RUT", "Rol"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0);

        for (Personal p : dao.listarTodos()) {
            Object[] fila = {p.getIdPersonal(), p.getNombre(), p.getRut(), p.getRol()};
            modelo.addRow(fila);
        }
        tbl_personal.setModel(modelo);
    }

    /**
     * Limpia todos los campos editables del formulario de personal, vaciando
     * los textos y restableciendo el combo de rol al primer elemento.
     */
