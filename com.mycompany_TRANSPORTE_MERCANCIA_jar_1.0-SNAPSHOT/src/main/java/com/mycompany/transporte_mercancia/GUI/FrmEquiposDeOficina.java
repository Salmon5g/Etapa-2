    /**
     * Maneja el evento del botón "Registrar". Registra un nuevo equipo de
     * oficina en el sistema con los datos ingresados en el formulario.
     * <p>
     * Realiza las siguientes validaciones y operaciones:
     * <ul>
     * <li>Verifica que los campos obligatorios no estén vacíos.</li>
     * <li>Verifica que el número de serie no exista previamente en la BD.</li>
     * <li>Persiste el equipo usando
     * {@link DAOEquipoOficina#create(EquipoOficina)}.</li>
     * <li>Recarga la tabla y limpia el formulario tras el registro
     * exitoso.</li>
     * </ul>
     *
     * @param evt Evento de acción generado por el botón bt_registrar_equipo.
     */
    private void bt_registrar_equipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_registrar_equipoActionPerformed
        String marca = txt_marca.getText().trim();
        String numeroSerie = txt_numero_serie.getText().trim().toUpperCase();
        String tipo = txt_tipo.getText().trim();  // ← también se agrega .trim()
        String estado = (String) cmb_estado.getSelectedItem();

        // ── Validación campos vacíos ────────────────────────────────────────
        if (marca.isEmpty() || numeroSerie.isEmpty() || tipo.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe completar todos los campos obligatorios (Tipo, Marca y N° Serie).",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            if (tipo.isEmpty()) {
                txt_tipo.requestFocus();  // enfoca el campo vacío
            }
            return;
        }

        // ── Validación formato número de serie ──────────────────────────────
        if (!numeroSerie.matches("[A-Z0-9\\-]{4,30}")) {
            JOptionPane.showMessageDialog(this,
                    "El N° de serie solo puede contener letras, números y guiones (-).\n"
                    + "Mínimo 4 caracteres, máximo 30.",
                    "Formato inválido", JOptionPane.WARNING_MESSAGE);
            txt_numero_serie.requestFocus();
            return;
        }

        // ── Validación número de serie duplicado ────────────────────────────
        if (daoEquipo.existeNumeroSerie(numeroSerie)) {
            JOptionPane.showMessageDialog(this,
                    "El número de serie \"" + numeroSerie + "\" ya existe en el sistema.",
                    "N° Serie duplicado", JOptionPane.WARNING_MESSAGE);
            txt_numero_serie.requestFocus();
            return;
        }

        // ── Crear equipo ────────────────────────────────────────────────────
        EquipoOficina eq = new EquipoOficina();
        eq.setTipo(tipo);
        eq.setMarca(marca.toUpperCase());
        eq.setNumeroSerie(numeroSerie);
        eq.setEstado(estado);

        daoEquipo.create(eq);

        JOptionPane.showMessageDialog(this, "Equipo registrado correctamente.");
        cargarTabla();
        limpiarFormulario();
    }//GEN-LAST:event_bt_registrar_equipoActionPerformed
    /**
     * Maneja el evento del botón "Modificar". Actualiza los datos del equipo
     * seleccionado en la base de datos.
     * <p>
     * Realiza las siguientes operaciones:
     * <ul>
     * <li>Busca el equipo por su ID usando
     * {@link DAOEquipoOficina#buscarPorId(int)}.</li>
     * <li>Verifica que los campos obligatorios no estén vacíos.</li>
     * <li>Verifica que el N° de serie no esté duplicado (excluyendo el equipo
     * actual).</li>
     * <li>Actualiza los campos y persiste los cambios con
     * {@link DAOEquipoOficina#update(EquipoOficina)}.</li>
     * <li>Recarga la tabla y limpia el formulario tras la actualización
     * exitosa.</li>
     * </ul>
     *
     * @param evt Evento de acción generado por el botón bt_modificar_equipo.
     */
    private void bt_modificar_equipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modificar_equipoActionPerformed
        Integer idSeleccionado = Integer.parseInt(txt_id_equipo.getText());

        EquipoOficina eq = daoEquipo.buscarPorId(idSeleccionado);
        if (eq == null) {
            JOptionPane.showMessageDialog(this,
                    "No se encontró el equipo a modificar.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String marca = txt_marca.getText().trim();
        String numeroSerie = txt_numero_serie.getText().trim().toUpperCase();
        String tipo = (String) txt_tipo.getText();
        String estado = (String) cmb_estado.getSelectedItem();

// ── Validación campos vacíos ────────────────────────────────────────
        if (marca.isEmpty() || numeroSerie.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe completar todos los campos obligatorios (Marca y N° Serie).",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

// ── Validación formato número de serie ──────────────────────────────
        if (!numeroSerie.matches("[A-Z0-9\\-]{4,30}")) {
            JOptionPane.showMessageDialog(this,
                    "El N° de serie solo puede contener letras, números y guiones (-).\n"
                    + "Mínimo 4 caracteres, máximo 30.",
                    "Formato inválido", JOptionPane.WARNING_MESSAGE);
            txt_numero_serie.requestFocus();
            return;
        }

// ── Validación N° serie duplicado (excluye el equipo actual) ────────
        if (!numeroSerie.equals(eq.getNumeroSerie()) && daoEquipo.existeNumeroSerie(numeroSerie)) {
            JOptionPane.showMessageDialog(this,
                    "El número de serie \"" + numeroSerie + "\" ya pertenece a otro equipo.",
                    "N° Serie duplicado", JOptionPane.WARNING_MESSAGE);
            txt_numero_serie.requestFocus();
            return;
        }
        // ── Validación campos vacíos ────────────────────────────────────────
        if (marca.isEmpty() || numeroSerie.isEmpty() || tipo.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe completar todos los campos obligatorios (Tipo, Marca y N° Serie).",
                    "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            if (tipo.isEmpty()) {
                txt_tipo.requestFocus();  // enfoca el campo vacío
            }
            return;
        }

// ── Aplicar cambios ─────────────────────────────────────────────────
        eq.setTipo(tipo);
        eq.setMarca(marca);
        eq.setNumeroSerie(numeroSerie);
        eq.setEstado(estado);

        daoEquipo.update(eq);

        JOptionPane.showMessageDialog(this, "Equipo modificado correctamente.");
        cargarTabla();
        activarModoNuevo();
        limpiarFormulario();
    }//GEN-LAST:event_bt_modificar_equipoActionPerformed

