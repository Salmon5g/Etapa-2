 /**
     * Carga y actualiza la tabla {@code tbl_equipos_oficina} con todos los
     * equipos registrados en la base de datos.
     * <p>
     * Las columnas mostradas son: ID, Tipo, Marca, N° Serie y Estado.
     */
    public void cargarTabla() {
        Object[] nombresColumnas = {"ID", "Tipo", "Marca", "N° Serie", "Estado"};
        DefaultTableModel modelo = new DefaultTableModel(nombresColumnas, 0);

        for (EquipoOficina eq : daoEquipo.listarTodos()) {
            Object[] fila = {
                eq.getIdEquipo(),
                eq.getTipo(),
                eq.getMarca(),
                eq.getNumeroSerie(),
                eq.getEstado()
            };
            modelo.addRow(fila);
        }

        this.tbl_equipos_oficina.setModel(modelo);
    }

    /**
     * Limpia todos los campos de entrada del formulario de equipos,
     * restableciendo los textos a vacío y los combos a su primera opción.
     */
    private void limpiarFormulario() {
        this.txt_id_equipo.setText("");
        this.txt_marca.setText("");
        this.txt_numero_serie.setText("");
        this.cmb_tipo.setSelectedIndex(0);
        this.cmb_estado.setSelectedIndex(0);
    }

    /**
     * Maneja el evento de clic sobre una fila de la tabla de equipos. Obtiene
     * el ID del equipo seleccionado y rellena automáticamente los campos del
     * formulario con los datos correspondientes.
     *
     * @param evt Evento de ratón generado al hacer clic en la tabla.
     */
    private void tbl_equipos_oficinaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_equipos_oficinaMouseClicked
        int selectedRow = this.tbl_equipos_oficina.getSelectedRow();

        if (selectedRow != -1) {
            Integer idSeleccionado = (Integer) this.tbl_equipos_oficina.getValueAt(selectedRow, 0);
            this.txt_id_equipo.setText(idSeleccionado.toString());

            EquipoOficina eq = daoEquipo.buscarPorId(idSeleccionado);
            if (eq != null) {
                this.cmb_tipo.setSelectedItem(eq.getTipo());
                this.txt_marca.setText(eq.getMarca());
                this.txt_numero_serie.setText(eq.getNumeroSerie());
                this.cmb_estado.setSelectedItem(eq.getEstado());
            }

            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_equipos_oficinaMouseClicked

