 /**
     * Carga y actualiza la tabla {@code tbl_equipos_oficina} con todos los
     * equipos registrados en la base de datos.
     * <p>
     * Las columnas mostradas son: ID, Tipo, Marca, N° Serie y Estado.
     */
    public void cargarTabla() {
        Object[] nombresColumnas = {"ID", "Tipo", "Marca", "N° Serie", "Estado"};
        DefaultTableModel modelo = new DefaultTableModel(nombresColumnas, 0);

        for (EquipoOficina eq : daoEquipo.listarTodos()) {
            Object[] fila = {
                eq.getIdEquipo(),
                eq.getTipo(),
                eq.getMarca(),
                eq.getNumeroSerie(),
                eq.getEstado()
            };
            modelo.addRow(fila);
        }

        this.tbl_equipos_oficina.setModel(modelo);
    }

