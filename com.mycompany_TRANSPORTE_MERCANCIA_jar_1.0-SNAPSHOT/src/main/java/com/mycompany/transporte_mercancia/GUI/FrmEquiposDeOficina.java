    /**
     * Activa el modo edición en el formulario, habilitando los botones de
     * cancelar, modificar y eliminar, y deshabilitando el botón de registrar.
     */
    private void activarModoEditar() {
        this.bt_cancelar.setEnabled(true);
        this.bt_modificar_equipo.setEnabled(true);
        this.bt_eliminar_equipo.setEnabled(true);
        this.bt_registrar_equipo.setEnabled(false);
    }

    /**
     * Activa el modo nuevo para ingresar un equipo.
     */
    private void activarModoNuevo() {
        this.bt_cancelar.setEnabled(false);
        this.bt_modificar_equipo.setEnabled(false);
        this.bt_eliminar_equipo.setEnabled(false);
        this.bt_registrar_equipo.setEnabled(true);
    }

