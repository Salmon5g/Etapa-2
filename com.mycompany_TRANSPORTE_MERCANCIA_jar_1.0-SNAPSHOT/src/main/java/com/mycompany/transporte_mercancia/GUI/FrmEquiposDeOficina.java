    /**
     * Maneja el evento de clic sobre una fila de la tabla de equipos. Obtiene
     * el ID del equipo seleccionado y rellena automáticamente los campos del
     * formulario con los datos correspondientes.
     *
     * @param evt Evento de ratón generado al hacer clic en la tabla.
     */
    private void tbl_equipos_oficinaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_equipos_oficinaMouseClicked
        int selectedRow = this.tbl_equipos_oficina.getSelectedRow();

        if (selectedRow != -1) {
            Integer idSeleccionado = (Integer) this.tbl_equipos_oficina.getValueAt(selectedRow, 0);
            this.txt_id_equipo.setText(idSeleccionado.toString());

            EquipoOficina eq = daoEquipo.buscarPorId(idSeleccionado);
            if (eq != null) {
                this.txt_tipo.setText(eq.getTipo());
                this.txt_marca.setText(eq.getMarca());
                this.txt_numero_serie.setText(eq.getNumeroSerie());
                this.cmb_estado.setSelectedItem(eq.getEstado());
            }

            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_equipos_oficinaMouseClicked

