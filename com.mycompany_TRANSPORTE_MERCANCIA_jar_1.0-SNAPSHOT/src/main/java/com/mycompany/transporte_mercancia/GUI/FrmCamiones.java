    /**
     * Valida que la matrícula tenga el formato chileno correcto.
     * <ul>
     * <li>Formato antiguo: 2 letras + 4 dígitos → ej. AB1234</li>
     * <li>Formato nuevo: 4 letras + 2 dígitos → ej. ABCD12</li>
     * </ul>
     * Se acepta con o sin guion (AB-1234 / ABCD-12).
     *
     * @param matricula texto ingresado por el usuario.
     * @return {@code true} si el formato es válido; {@code false} en caso
     * contrario.
     */
    private boolean validarFormatoMatricula(String matricula) {
        if (matricula == null) {
            return false;
        }
        // Normalizar: mayúsculas y quitar guion
        String m = matricula.toUpperCase().replace("-", "").trim();
        return m.matches("^[A-Z]{2}[0-9]{4}$") // Formato antiguo: AB1234
                || m.matches("^[A-Z]{4}[0-9]{2}$");   // Formato nuevo:  ABCD12
    }

    /**
     * Normaliza la matrícula a mayúsculas sin guion.
     */
    private String normalizarMatricula(String matricula) {
        return matricula.toUpperCase().replace("-", "").trim();
    }

