    /**
     * Maneja el evento del botón "Guardar". Registra un nuevo camión en el
     * sistema con los datos ingresados en el formulario.
     * <p>
     * Realiza las siguientes validaciones y operaciones:
     * <ul>
     * <li>Verifica que la matrícula no exista previamente en la BD.</li>
     * <li>Inicializa el kilometraje total en 0 si es {@code null}.</li>
     * <li>Persiste el camión usando {@link DAOCamion#create(Camion)}.</li>
     * <li>Recarga la tabla y el combo de conductores.</li>
     * <li>Limpia el formulario tras el registro exitoso.</li>
     * </ul>
     *
     * @param evt Evento de acción generado por el botón bt_guardar.
     */

    private void bt_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardarActionPerformed
        String matriculaRaw = txt_matricula.getText().trim();

        // ── Validación año ──────────────────────────────────────────────────
        int anio;
        try {
            anio = Integer.parseInt(txt_anio.getText().trim());
            if (anio <= 0) {
                JOptionPane.showMessageDialog(this,
                        "El año debe ser un número positivo.",
                        "Año inválido", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "El año ingresado no es válido. Ingrese solo números.",
                    "Año inválido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ── Validación formato matrícula ────────────────────────────────────
        if (!validarFormatoMatricula(matriculaRaw)) {
            JOptionPane.showMessageDialog(this,
                    "Formato de matrícula inválido.\n"
                    + "Use el formato antiguo (AB1234) o nuevo (ABCD12).\n"
                    + "El guion es opcional.",
                    "Matrícula inválida", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String matriculaNormalizada = normalizarMatricula(matriculaRaw);

        // ── Validación duplicado ────────────────────────────────────────────
        if (dp.existeMatricula(matriculaNormalizada)) {
            JOptionPane.showMessageDialog(this,
                    "La matrícula ingresada ya existe en el sistema.",
                    "Matrícula duplicada", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ── Crear camión ────────────────────────────────────────────────────
        Camion c = new Camion();
        c.setAnio(anio);
        c.setMarca(txt_marca.getText());
        c.setModelo((String) cmb_modelo.getSelectedItem());
        c.setMatricula(matriculaNormalizada);   // guardamos normalizada

        Personal seleccionado = (Personal) cmb_conductor.getSelectedItem();
        c.setConductor(seleccionado);

        if (c.getKilometrajeTotal() == null) {
            c.setKilometrajeTotal(0);
        }

        dp.create(c);

        JOptionPane.showMessageDialog(this, "Camión registrado correctamente.");
        cargarTabla();
        cargarComboConductores();
        limpiarFormulario();
    }//GEN-LAST:event_bt_guardarActionPerformed
    /**
     * Maneja el evento de clic sobre una fila de la tabla de camiones. Obtiene
     * el ID del camión seleccionado y rellena automáticamente los campos del
     * formulario con los datos correspondientes (modelo, marca, año, conductor
     * y matrícula).
     *
     * @param evt Evento de ratón generado al hacer clic en la tabla
     * tbl_camiones.
     */
    private void tbl_camionesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_camionesMouseClicked
        int selectedRow = this.tbl_camiones.getSelectedRow();

        if (selectedRow != -1) {

            Object cellValue = this.tbl_camiones.getValueAt(selectedRow, 0);

            Integer id_seleccionado = (Integer) cellValue;
            this.txt_id_camion.setText(id_seleccionado.toString());

            for (Camion c : dp.listarTodos()) {
                if (c.getIdCamion() == id_seleccionado) {
                    this.cmb_modelo.setSelectedItem(c.getModelo());
                    this.txt_marca.setText(c.getMarca());
                    this.txt_anio.setText(c.getAnio().toString());
                    this.cmb_conductor.setSelectedItem(c.getConductor());
                    this.txt_matricula.setText(c.getMatricula());
                }
            }

            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_camionesMouseClicked
    /**
     * Maneja el evento del botón "Guardar Cambios". Actualiza los datos del
     * camión seleccionado en la base de datos.
     * <p>
     * Realiza las siguientes operaciones:
     * <ul>
     * <li>Busca el camión por su ID usando
     * {@link DAOCamion#buscarPorId(Integer)}.</li>
     * <li>Verifica que la matrícula nueva no esté duplicada en el sistema.</li>
     * <li>Actualiza todos los campos del camión y persiste los cambios con
     * {@link DAOCamion#update(Camion)}.</li>
     * <li>Recarga la tabla y el combo de conductores.</li>
     * <li>Limpia el formulario tras la actualización exitosa.</li>
     * </ul>
     *
     * @param evt Evento de acción generado por el botón bt_guardar_cambios.
     */
    private void bt_guardar_cambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_guardar_cambiosActionPerformed
        String matriculaRaw = txt_matricula.getText().trim();
        Integer id_seleccionado = Integer.parseInt(txt_id_camion.getText());

        Camion c = dp.buscarPorId(id_seleccionado);
        if (c == null) {
            JOptionPane.showMessageDialog(this,
                    "No se encontró el camión a modificar.",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // ── Validación año ──────────────────────────────────────────────────
        int anio;
        try {
            anio = Integer.parseInt(txt_anio.getText().trim());
            if (anio <= 0) {
                JOptionPane.showMessageDialog(this,
                        "El año debe ser un número positivo.",
                        "Año inválido", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "El año ingresado no es válido. Ingrese solo números.",
                    "Año inválido", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ── Validación formato matrícula ────────────────────────────────────
        if (!validarFormatoMatricula(matriculaRaw)) {
            JOptionPane.showMessageDialog(this,
                    "Formato de matrícula inválido.\n"
                    + "Use el formato antiguo (AB1234) o nuevo (ABCD12).\n"
                    + "El guion es opcional.",
                    "Matrícula inválida", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String matriculaNormalizada = normalizarMatricula(matriculaRaw);

        // ── Validación duplicado (excluye el camión actual) ─────────────────
        if (dp.existeMatricula(matriculaNormalizada, id_seleccionado)) {
            JOptionPane.showMessageDialog(this,
                    "La matrícula ingresada ya pertenece a otro camión.",
                    "Matrícula duplicada", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ── Aplicar cambios ─────────────────────────────────────────────────
        c.setMarca(txt_marca.getText());
        c.setModelo((String) cmb_modelo.getSelectedItem());
        c.setAnio(anio);
        c.setMatricula(matriculaNormalizada);   // guardamos normalizada

        Personal seleccionado = (Personal) cmb_conductor.getSelectedItem();
        c.setConductor(seleccionado);

        dp.update(c);

        JOptionPane.showMessageDialog(this, "Camión modificado correctamente.");
        cargarTabla();
        cargarComboConductores();
        activarModoNuevo();
        limpiarFormulario();
    }//GEN-LAST:event_bt_guardar_cambiosActionPerformed
