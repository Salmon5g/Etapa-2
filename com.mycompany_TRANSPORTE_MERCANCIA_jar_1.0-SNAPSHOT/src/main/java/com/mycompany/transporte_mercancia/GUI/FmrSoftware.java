    private void bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_agregarActionPerformed
        // TODO add your handling code here:
        String nombre = txt_nombre.getText().trim();
        String version = txt_version.getText().trim();
        String fabricante = txt_fabricante.getText().trim();

        if (nombre.isEmpty() || version.isEmpty() || fabricante.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Complete todos los campos");
            return;
        }

        if (dao.existeSoftware(nombre, version)) {
            JOptionPane.showMessageDialog(this,
                    "El software ya existe");
            return;
        }

        Software s = new Software();
        s.setNombre(nombre);
        s.setVersion(version);
        s.setFabricante(fabricante);

        dao.create(s);
        JOptionPane.showMessageDialog(this,
                "Software registrado correctamente");

        cargarTabla();
        limpiarFormulario();
    }//GEN-LAST:event_bt_agregarActionPerformed

    private void activarModoNuevo() {

        bt_agregar.setEnabled(true);
        bt_modificar.setEnabled(false);
        bt_eliminar.setEnabled(false);
    }

    private void bt_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_modificarActionPerformed
        // TODO add your handling code here:
        int id
                = Integer.parseInt(txt_id.getText());

        String nombre = txt_nombre.getText().trim();
        String version = txt_version.getText().trim();
        String fabricante = txt_fabricante.getText().trim();

        if (dao.existeSoftware(nombre, version, id)) {
            JOptionPane.showMessageDialog(this,
                    "Ya existe otro software igual");
            return;
        }

        Software s = new Software();

        s.setIdSoftware(id);
        s.setNombre(nombre);
        s.setVersion(version);
        s.setFabricante(fabricante);
        dao.update(s);
        JOptionPane.showMessageDialog(this,
                "Software modificado correctamente");

        cargarTabla();
        activarModoNuevo();
        limpiarFormulario();
    }//GEN-LAST:event_bt_modificarActionPerformed

    private void bt_cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_cancelarActionPerformed
        // TODO add your handling code here:
        limpiarFormulario();
        activarModoNuevo();
        tbl_software.clearSelection();
    }//GEN-LAST:event_bt_cancelarActionPerformed

    private void limpiarFormulario() {

        txt_id.setText("");
        txt_nombre.setText("");
        txt_version.setText("");
        txt_fabricante.setText("");
    }

    public void cargarTabla() {

        Object columnas[] = {"ID", "Nombre", "Version", "Fabricante", "Fecha Registro"};
        DefaultTableModel modelo
                = new DefaultTableModel(columnas, 0);
        for (Software s : dao.listarTodos()) {
            Object fila[] = {s.getIdSoftware(), s.getNombre(), s.getVersion(), s.getFabricante(), s.getFechaRegistro()};
            modelo.addRow(fila);
        }
        tbl_software.setModel(modelo);
    }


    private void bt_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_eliminarActionPerformed
        // TODO add your handling code here:
        int id
                = Integer.parseInt(txt_id.getText());
        dao.delete(id);
        JOptionPane.showMessageDialog(this,
                "Software eliminado correctamente");

        cargarTabla();
        activarModoNuevo();
        limpiarFormulario();
    }//GEN-LAST:event_bt_eliminarActionPerformed

    private void tbl_softwareMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_softwareMouseClicked
        int fila = tbl_software.getSelectedRow();

        if (fila != -1) {

            txt_id.setText(
                    tbl_software.getValueAt(fila, 0).toString());

            txt_nombre.setText(
                    tbl_software.getValueAt(fila, 1).toString());

            txt_version.setText(
                    tbl_software.getValueAt(fila, 2).toString());

            txt_fabricante.setText(
                    tbl_software.getValueAt(fila, 3).toString());

            activarModoEditar();
        }
    }//GEN-LAST:event_tbl_softwareMouseClicked

    private void activarModoEditar() {
        bt_agregar.setEnabled(false);
        bt_modificar.setEnabled(true);
        bt_eliminar.setEnabled(true);
    }


