   /**
 * Registra un nuevo software en la base de datos.
 * <p>
 * Este método obtiene los datos ingresados en el formulario,
 * valida los campos, verifica que no exista un software
 * duplicado y posteriormente guarda la información en la
 * base de datos MySQL.
 * </p>
 *
 * @param evt evento generado al presionar el botón agregar
 */
    private void bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_agregarActionPerformed
        String nombre = txt_nombre.getText().trim();
        String version = txt_version.getText().trim();
        String fabricante = txt_fabricante.getText().trim();

        if (!validarCampos(nombre, version, fabricante)) {
            return;
        }

        if (dao.existeSoftware(nombre, version)) {
            JOptionPane.showMessageDialog(this, "Ya existe un software con ese nombre y versión.");
            txt_nombre.requestFocus();
            return;
        }

        Software s = new Software();
        s.setNombre(nombre);
        s.setVersion(version);
        s.setFabricante(fabricante);

        dao.create(s);
        JOptionPane.showMessageDialog(this, "Software registrado correctamente.");
        cargarTabla();
        limpiarFormulario();
    }//GEN-LAST:event_bt_agregarActionPerformed

